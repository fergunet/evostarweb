<h2>Knowledge Incorporation in Evolutionary Computation</h2>

<?php include('common_to_all.php') ?>
<?php include('common_to_apps.php') ?>

This track, organized under the aegis of the H2020 FET-OPEN project
<a href="http://cordis.europa.eu/project/rcn/196968_en.html">"PHOENIX: Exploring the Unknown through Reincarnation and Co-
evolution"</a>, aims at bringing together researchers and practitioners from
all application domains dealing with the incorporation of knowledge into
evolutionary techniques. We seek high-quality contributions focused on
multi-disciplinary approaches combining Evolutionary Computation,
knowledge representation, knowledge transfer, case-based reasoning,
and real-world applications thereof. Topics of interest include but are not
limited to:

<ul>
<li>Incorporation of domain knowledge into evolutionary algorithms</li>
<li>Novel techniques to identify and elicit explicit and/or implicit expert knowledge relevant to evolutionary algorithms</li>
<li>Innovative knowledge representation strategies for Evolutionary Computation</li>
<li>Case-based reasoning applied to evolutionary algorithms</li>
<li>Knowledge transfer across applications and domains</li>
<li>Methods to account for uncertainties and contradictions in knowledge</li>
<li>Knowledge-informed surrogate-assisted evolutionary algorithms</li>
<li>Lifetime learning and Interactive Evolutionary Computation</li>
<li>Memetic computing and domain-specific evolutionary techniques</li>
<li>Real-world applications of knowledge-based Evolutionary Computation</li>

</ul>


