<h3>PUBLICATION DETAILS</h3>



    <p>Accepted papers will appear in the proceedings of Evo<span class="red">Applications</span>, 
      published in a volume of the Springer Lecture Notes in Computer Science, 
      which will be available at the Conference. Submissions must be original and 
      not published elsewhere. The submissions will be peer reviewed by at least 
      three members of the program committee. The authors of accepted papers will 
      have to improve their paper on the basis of the reviewers comments and will 
      be asked to send a camera ready version of their manuscripts. At least one 
      author of each accepted work has to register for the conference and attend 
      the conference and present the work.  </p>

      <p>
This year EvoApplications is accepting two kinds of submission: full papers and short papers. <strong>Full papers</strong> require novel and complete research work and have a limit of 16 pages. <strong>Short papers</strong> should present complete research or interesting preliminary results and have a limit of 8 pages. Both types of submission will undergo the same double blind review process and all accepted papers will be included in the LNCS proceedings. Based on reviewersâ€™ assessment, accepted full papers will be presented as full length talks, and authors of accepted short papers will present a short talk as well as present a poster. All authors of accepted papers will be given the opportunity to further disseminate their work in poster sessions.
      </p>


