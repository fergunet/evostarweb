Join us in Parma  for Evo<span class="red">BIO</span>, a multidisciplinary track that brings together researchers working in Bioinformatics, Medical Applications and Computational Biology that apply advanced techniques coming from Evolutionary Computation, Machine Learning, and Data Mining to address important problems in medical applications and biology, from the molecular and genomic dimension, to the individual and population level. Each accepted paper will be presented orally or as a poster at the conference and will be printed in the proceedings published by Springer Verlag in the LNCS series.  



Possible topics of interest include, but are not limited to:


<ul>
<li>Medical imaging,signal processing and text analysis</li>
<li>Data mining medical data and records</li>
<li>Clinical expert systems</li>
<li>Modelling and simulation of medical processes</li>
<li>Drug description analysis</li>
<li>Patient-centric care</li>
<li>Interpreting genomic or metagenomic data</li>
<li>Discovering regulatory or expression pathways</li>
<li>Rational drug design and personalized medicine</li>
<li>Modeling ecosystems or population dynamics</li>
<li>Discovering genome-disease or genome-phenotype associations</li>
<li>Biomedical text/data mining and visualization</li>
<li>Network biology/medicine</li>
<li>Omics data analysis and functional genomics for complex diseases</li>
<li>Gene-gene interactions and gene-environment interactions for disease association analysis</li>
<li>Protein Structure Prediction</li>
<li>Assembling next generation sequence data</li>
</ul>

