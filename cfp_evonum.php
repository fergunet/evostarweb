<h2>Bio-inspired Algorithms for Continuous Parameter Optimisation</h2>
<?php include('common_to_all.php') ?>
<?php include('common_to_apps.php') ?>

<blockquote>''The main application areas of EC techniques [in industry] are multi-objective optimization, classification, data mining and numerical optimization''. [1]</blockquote>

<p>Many engineering problems of both theoretical and practical interest involve choosing the best configuration of a set of parameters to achieve a specified objective. Numerical optimisation refers to the case when these parameters take continuous real values, as opposed to combinatorial optimisation, which deals with discrete values. Examples include designing production processes for maximum efficiency, optimal parameter adjustment for controllers and many others. Evo<span class="red">NUM</span> focuses on such problems.</p>

<p>
We seek high quality papers involving the application of bio-inspired algorithms (genetic algorithms, genetic programming, evolution strategies, differential evolution, particle swarm optimization, evolutionary programming, simulated annealing and their hybrids) to continuous optimisation problems in engineering. We also welcome cross-fertilisation between Nature-inspired algorithms and more classical numerical optimisation algorithms.
</p>
<em>[1] GS Hornby and T Yu, "EC Practitioners: Results of the First Survey", SIGEVOlution, Newsletter of the ACM Special Interest Group on Genetic and Evolutionary Computation, Vol. 2(1), Spring 2007 www.sigevolution.org</em>

<h3>Areas of Interest and Contributions</h3>

Evo<span class="red">NUM</span> deals with engineering applications where continuous parameters or functions have to be optimised, in fields such as control, chemistry, agriculture, electricity, building and construction, energy, aerospace engineering, design optimisation, etc.

Evo<span class="red">NUM</span> aims to cover areas that include but are not limited to:
<ul>
<li>Local learning of parameters</li>
<li>Mechanisms to incorporate constraints</li>
<li>Theoretical developments</li>
<li>Performance measures and performance analysis</li>
<li>Benchmark problems</li>
</ul>

<h3>PUBLICATION DETAILS</h3>

Accepted papers will appear in the proceedings of EvoStar, published in a volume of the Springer Lecture Notes in Computer Science, which will be available at the Conference.Submissions must be original and not published elsewhere. The submissions will be peer reviewed by at least three members of the program committee. The authors of accepted papers will have to improve their paper on the basis of the reviewers comments and will be asked to send a camera ready version of their manuscripts. At least one author of each accepted work has to register for the conference and attend the conference and present the work.The reviewing process will be double-blind, please omit information about the authors in the submitted paper.

