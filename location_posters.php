<h4>AV at the venue</h4>
You are encouraged to bring your own laptop or tablet for your presentation, along with
appropriate adaptors and chargers for your equipment as EvoStar does not provide these.
All LCD projectors (beamers) have a VGA input. Some may provide HDMI, but this is not
guaranteed. Voltage in Italy is 220v, and the two-pin plug is used, similar to most
European countries.

<h4>POSTERS</h4>
The poster session will take place in conjunction with the EvoStar reception on
Wednesday evening, 4 April from 1830-2000. Poster boards are 1 metre wide by 2 metres
tall and can accommodate posters up to A0 size. Tape can be used to fix posters and this
will be available at the conference venue.
