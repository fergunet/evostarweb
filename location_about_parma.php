EvoStar 2018 is being held at the historical central building of the University of Parma in via Università 1, right in the city center and within walking distance from most central hotels.

<p>
Parma is located in northern Italy, in Emilia-Romagna, halfway between Bologna and Milan. This elegant city owes its refined atmosphere - one that can only be breathed in a “petite capitale” - to its 18th century bond with the French royal family: the central Parco Ducale reproduces the design of the Jardin des Tuileries in Paris. With its center rich in art, parks and treasures from different ages, Parma is a very welcoming place.
</p>
<p>
Widely known for its gastronomic heritage, Parma has been selected as “food creativity town” by UNESCO, and is world-famous for products like Parmigiano Reggiano cheese and Parma ham.
