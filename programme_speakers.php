<h3>Opening talk: Adversarial Dynamics:  Understanding Them Now is Important as Ever</h3>
<h4> Una-May O'Reilly,AnyScale Learning for All Group, MIT Computer Science and Artificial Intelligence Lab</h4>
<h4> 09:45-10:45, Wednesday 4 April 2018</h4>


<div>
<img src="img/unamay.jpg" width="200px" align="left" style="margin:10px"/>
<strong>Abstract:</strong> The world abounds with adversarial relationships - opponents squaring off in contests and disputes. They are found in nature (predator vs prey) and in human society (gaming, wars, cyber security). They shape important outcomes and influence the character of our existence. I will describe  my research agenda on the dynamics of adversarial relationships and understanding how optimization and evolutionary computation comprise a useful lens through which they can be examined.
<p>
<strong>Bio:</strong> Una-May O'Reilly leads the AnyScale Learning For All (ALFA) group at MIT. She has expertise in scalable machine learning, evolutionary algorithms, and frameworks for large-scale, automated knowledge mining, prediction and analytics. She educates the forthcoming generation of data scientists, teaching them how develop state of art techniques that address the challenges spanning data integration to knowledge extraction.
</p>
