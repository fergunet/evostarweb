<h3>Closing talk: Evolution, Art and Sex</h3>

<h4>Penousal Machado, University of Coimbra</h4>
<h4>12:00-13:00, Friday 6 April 2018</h4>

<div>
<img src="organisers/penousal_machado.jpg" width="200px" align="right" style="margin:10px"/>
<strong>Abstract: </strong>Applying the techniques of Evolutionary Computation to artistic expression in both the visual arts and music over a period of several years has generated a timeline of what might be termed cultural artefacts.  This suggested to me that the field - its past and current methodology, its trends and challenges - could be viewed through the prism of archeology.   We will focus on one of the most intractable problems in the field: fitness assignment, analysing this challenge from the perspective of  a human user interacting with an evolutionary system.  How do Machine Learning, Evolutionary Computation and HCI techniques combine to create a Computer Aided Creativity system that responds to the artistic intentions of the user?
<p>
<strong>Bio: </strong> Penousal Machado leads the Cognitive and Media Systems group at the University of Coimbra.  His research interests include Evolutionary Computation, Computational Creativity, and Evolutionary Machine Learning. In addition to the numerous scientific papers in these areas, his works have been presented in venues such as the National Museum of Contemporary Art (Portugal) and the “Talk to me” exhibition of the Museum of Modern Art, NY (MoMA).
</p>
