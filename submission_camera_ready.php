<small>
<strong>IMPORTANT NOTICE: To be published in the proceedings, at least one author must REGISTER and PAY for the conference (before 9 February 2018), and also ATTEND to present his/her paper.</strong>
<p>
Please follow Springer's LNCS instructions when preparing your paper.  <a href="ftp://ftp.springernature.com/cs-proceeding/svproc/guidelines/Springer_Guidelines_for_Authors_of_Proceedings.pdf">ftp://ftp.springernature.com/cs-proceeding/svproc/guidelines/Springer_Guidelines_for_Authors_of_Proceedings.pdf </a>
</p>

<p>
Remember that references should be ordered by appearance in the paper, using 'splncs.bst' as BibTeX style (if you use LaTeX) or generating a list with this order if you use Word. You are encouraged to include DOI identifiers in your references.
</p>
<p>
<strong>New in 2017:</strong> Authors can include their ORCID identifiers. See the instructions for details.
</p>
<p>
There are strict page limits for all of the 4 conferences collectively known as EvoStar
</p>
<strong>16 pages</strong>


<p>
Springer reprocesses all the contributions, meaning that you have to provide all source files and figures.
</p>
You should prepare a single tgz or zip-file containing a single directory with all of the necessary files (and all the necessary .sty files). <u>The name of the directory should be the last name of the first author.</u> The directory has to contain:
<ul>
<li><strong>Latex .tex file</strong> (named with the last name of the first author).</li>
<li><strong>Latex .bib and .bbl files</strong> (named with the last name of the first author).</li>
<li><strong>All figures:</strong> .png, .jpg, .pdf (using pdflatex) or .ps, .eps (using latex).</li>
<li><strong>Final .pdf file</strong> (named with the last name of the first author).</li>
<li><strong>.rtf file</strong> if another word processing program other than Latex was used (named with the last name of the first author).</li>
<li><strong>authors.txt</strong> : This file should have one line for each author of the paper using the following format: </li>
<em>
[first names of first author] [last name of first author] [email]<br/>
[first names of second author] [last name of second author] [email]<br/>
</em>
<p>
Please mark the contact author with * at the end of the line.
</p>
<p>
Example of a paper authored by Mario Rossi and Jos√© Salgado:<br/>
<em>
Mario Rossi mario.rossi@myuni.xy<br/>
Jos\'e Salgado jsalgado@hisuni.yx*<br/>
</em>
</p>
Note that in authors.txt any accent/special characters must be written in Latex style, e.g.: Jos\'e
<li><strong>abstract.txt</strong> : This file should have the following structure:</li>
first line: title of the paper
second line: blank
remaining lines: abstract of the paper (in ASCII without line breaks).

<li>Scanned signed consent to publish/copyright form as PDF using the following filename:</li>
[last_name_of_first_author]cr.pdf<br/>
(one author may sign on behalf of all authors of a paper).<br/>
</ul>
<p>
Download the LNCS consent to publish/copyright form <a href="http://resource-cms.springer.com/springer-cms/rest/v1/content/731196/data/v3">from here</a>. 
</p>

Other considerations:
<ul>
<li>When submitting a paper, please specify between <strong>three to five keywords</strong>.</li>
<li>EvoAPP's authors, please be sure to specify a <strong>secondary topic</strong>, i.e., the second more relevant track.</li>
<li>In camera ready, and using LaTeX, authors should fill up \authorrunning and \titlerunning, without activating them (commented).</li>
Example:<br/>
<em>
%\authorrunning{A. Uthor et al.}<br/>
%\titlerunning{My beautiful paper}<br/>
</em>
<li>In camera ready, authors should not enable page numbering.</li>
<li>If using LaTeX, please include a bibtex file, even if the bibliography is embedded into the source .tex file(s)</li>
</ul>
<p>When preparing your camera-ready, please make sure that you omit all headers (no page numbers, no running title, no running authors). Springer will activate running title and authors in reformatting the proceedings. Therefore, specify running title and running authors in the .tex source using comments. For Word users, add the specifications without activating headers.</p>
<p>Authors not following the instructions may result in the paper being excluded from the proceedings.</p>
Also, before <strong>15 January 2018</strong>, you also have to send an E-mail to evostar2018@gmail.com with the title:
"EvoStar18 attendance confirmation"
which includes the following information:
<ol>
<li>Name of (co)author who will attend EvoStar2018 </li>
<li>If attendee is a student, their advisor has been cc-ed into this mail</li>
<li>If a VISA is required for the attendee, the application process has started: Not-required / Yes / No.</li>
</ol>

In summary, you have to do each of the following 3 tasks no later than <strong>15 January</strong>:
<ul>
<li>Submit a zip file containing the paper including all source files (see above for details), an authors.txt file, an abstract.txt file and a signed consent to publish/copyright form as PDF</li>
<li>Send an email with the name of the (co)author who will attend and register at EvoStar 2018</li>
<li>At least one author must register and pay the conference fee by <strong>9 February 2018</strong>. Failure to do so could result in the paper being excluded from publication.</li>
</ul>


