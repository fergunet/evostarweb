Other considerations:<br/>
<ul>
<li>When submitting a paper, please specify at least <strong>three keyworkds</strong>.</li>
<li>EvoAPP's authors, please be sure to specify a <strong>secondary topic</strong>, i.e., the second more relevant track.</li>
<li>In camera ready, and using LaTeX, authors should fill up \authorrunning and \titlerunning, without activating them (commented).</li>
Example:<br/>
<em>%\authorrunning{A. Uthor et al.}</em><br/>
<em>%\titlerunning{My beautiful paper}</em><br/>
<li>In camera ready, authors should not enable page numbering.</li>
<li>If using LaTeX, please include a bibtex file, even if the bibliography is embedded into the source .tex file(s).</li>
</ul>



<p>
When preparing your camera-ready, please make sure that you omit all headers (no page numbers, no running title, no running
authors). Springer will
activate running title and authors in reformatting the proceedings. Therefore, specify running title and running authors in the .tex source using comments. For Word users, add the
specifications without activating headers.
</p>

<p>Authors not following the instructions may result in the paper being excluded from the proceedings.</p>


