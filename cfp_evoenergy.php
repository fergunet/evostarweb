<h3>Introduction</h3>

<p>Along with the worldwide incentive to reduce fossil and nuclear based power
generation, the number of distributed generators and other forms of
distributed energy resources which are installed in power networks has been
steadily increasing over the last years. This increased integration has
triggered a transformation of the energy system and challenges the
conventional operation of these networks.</p>

<p>On a network level, this transformation requires new control and communication
approaches, to guarantee the security of energy supplies as well as an optimal
exploitation of available resources. On a generator level, advanced control
strategies as well as morphological optimization (e.g., tuning of wind-blade
design) can help to assure an optimal performance of the generator.</p>

<p>EvoEnergy is intended as a platform for new, innovative computational
intelligence and nature-inspired techniques in the domain of energy-related
optimization research. We seek contributions ranging from new control concepts
for decentralized generation, strategies for their coordination in the network
to the morphological optimization of distributed generators.</p>

<h3>Topics</h3>

Topics of interest include, but are not limited to:
<ul>
<li>management of distributed generation and storage</li>
<li>evolutionary design and morphological optimization in energy systems</li>
<li>advanced distribution management system functionalities</li>
<li>Smart Grid monitoring and control</li>
<li>real time configurations of energy networks</li>
<li>distributed optimization in energy networks</li>
<li>grid planning and asset management</li>
<li>energy demand reduction through evolutionary design optimization</li>
</ul>

