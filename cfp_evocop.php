    <p>The 18th European Conference on Evolutionary Computation in Combinatorial Optimisation is a multidisciplinary conference that brings together researchers working on metaheuristics for solving difficult combinatorial optimisation problems appearing in various industrial, economic, and scientific domains. Prominent examples of metaheuristics include: evolutionary algorithms, simulated annealing, tabu search, scatter search and path relinking, memetic algorithms, ant colony and bee colony optimisation, particle swarm optimisation, variable neighbourhood search, iterated local search, greedy randomized adaptive search procedures, estimation of distribution algorithms, and hyperheuristics. Successfully solved problems include scheduling, timetabling, network design, transportation and distribution problems, vehicle routing, travelling salesman, graph problems, satisfiability, energy optimisation problems, packing problems, and planning problems.</p>


      <p>The Evo<span class="red">COP</span> 2018 conference will be held in the city of Parma, Italy. It will be held in conjunction with Euro<span class="red">GP</span> (the 19th European Conference on Genetic Programming), Evo<span class="red">MUSART</span> (5th European conference on evolutionary and biologically inspired music, sound, art and design) and Evo<span class="red">Applications</span> (specialist events on a range of evolutionary computation topics and applications), in a joint event collectively known as Evo<span class="red">*</span>.</p>

