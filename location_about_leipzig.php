<p>
Leipzig is located in the north-east of Germany, about 160km south of Berlin. It is the largest city in
the federal state of Saxony and looks back at more than 1000 years of history. Situated at the
crossing of two major medieval trading routes, Via Imperii connecting Italy with the North and Via
Regia connecting East and West, it was once considered as one of the major European centers of
trade, culture and learning. Following the reunification in 1990, Leipzig reestablished itself as an
important city of commerce, arts and science in Germany.
Today Leipzig is an easily likeable, green and young city with a rich cultural diversity. Particularly in
the city center we find many beautiful historical buildings from different architectural periods,
museums and parks. Also, lovers of classical music appreciate Leipzig due to the impact of Bach and
Mendelsohn-Bartholdy, who lived in the city for important parts of their lives.
</p>
