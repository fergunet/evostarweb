<h3 id="abstracts">Accepted paper abstracts</h3>
<p><strong>Title: A Multiple Expression Alignment Framework for Genetic Programming
</strong><br><strong>Author:</strong>   Leonardo Vanneschi,  Kristen Scott,  Mauro Castelli
<br><strong>Abstract:</strong> Alignment in the error space is a recent idea to exploit semantic awareness in genetic programming. In a previous contribution, the concepts of optimally aligned and optimally coplanar individuals were introduced, and it was shown that given optimally aligned, or optimally coplanar, individuals, it is possible to construct a globally optimal solution analytically. As a consequence, genetic programming methods, aimed at searching for optimally aligned, or optimally coplanar, individuals were introduced. In this paper, we critically discuss those methods, analyzing their major limitations and we propose new genetic programming systems aimed at overcoming those limitations. The presented experimental results, conducted on five real-life symbolic regression problems, show that the proposed algorithms outperform not only the existing methods based on the concept of alignment in the error space, but also geometric semantic genetic programming and standard genetic programming.





</p><p><strong>Title: Evolving Graphs by Graph Programming
</strong><br><strong>Author:</strong>   Timothy Atkinson,  Detlef Plump,  Susan Stepney
<br><strong>Abstract:</strong> Rule-based graph programming is a deep and rich topic. We
present an approach to exploiting the power of graph programming as a
representation and as an execution medium in an evolutionary algorithm
(EGGP). We demonstrate this power in comparison with Cartesian Genetic
Programming (CGP), showing that it is significantly more efficient
in terms of fitness evaluations on some classic benchmark problems. We
hypothesise that this is due to its ability to exploit the full graph structure,
leading to a richer mutation set, and outline future work to test
this hypothesis, and to exploit further the power of graph programming
within an EA.





</p><p><strong>Title: Pruning Techniques for Mixed Ensembles of Genetic Programming Models
</strong><br><strong>Author:</strong>   Mauro Castelli,  Ivo Gonçalves,  Luca Manzoni,  Leonardo Vanneschi
<br><strong>Abstract:</strong> The objective of this paper is to define an effective strategy for building an ensemble of Genetic Programming (GP) models. Ensemble methods are widely used in machine learning due to their features: they average out biases, they reduce the variance and they usually generalize better than single models. Despite these advantages, building ensemble of GP models is not a well-developed topic in the evolutionary computation community. To fill this gap, we propose a strategy that blends individuals produced by standard syntax-based GP and individuals produced by geometric semantic genetic programming, one of the newest semantics-based method developed in GP. In fact, recent literature showed that combining syntactic and semantics could improve the generalization ability of a GP model. Additionally, to improve the diversity of the GP models used to build up the ensemble, we propose different pruning criteria that are based on correlation and entropy, a commonly used measure in information theory. Experimental results, obtained over different complex problems, suggest that the pruning criteria based on correlation and entropy could be effective in improving the generalization ability of the ensemble model and in reducing the computational burden required to build it.






</p><p><strong>Title: Generating Redundant Features with Unsupervised Multi-Tree Genetic Programming
</strong><br><strong>Author:</strong>   Andrew Lensen,  Bing Xue,  Mengjie Zhang
<br><strong>Abstract:</strong> Recently, feature selection has become an increasingly important area of research due to the surge in high-dimensional datasets in all areas of modern life. A plethora of feature selection algorithms have been proposed, but it is difficult to truly analyse the quality of a given algorithm. Ideally, an algorithm would be evaluated by measuring how well it removes known bad features. Acquiring datasets with such features is inherently difficult, and so a common technique is to add synthetic bad features to an existing dataset. While adding noisy features is an easy task, it is very difficult to automatically add complex, redundant features. This work proposes one of the first approaches to generating redundant features, using a novel genetic programming approach. Initial experiments show that our proposed method can automatically create difficult, redundant features which have the potential to be used for creating high-quality feature selection benchmark datasets.





</p><p><strong>Title: Towards In Vivo Genetic Programming: Evolving Boolean Networks to Determine Cell States
</strong><br><strong>Author:</strong>   Nadia Taou,  Michael Lones
<br><strong>Abstract:</strong> Within the genetic programming community, there has been growing interest in the use of computational representations motivated by gene regulatory networks (GRNs). It is thought that these representations capture useful biological properties, such as evolvability and robustness, and thereby support the evolution of complex computational behaviours. However, computational evolution of GRNs also opens up opportunities to go in the opposite direction: designing programs that could one day be implemented in biological cells. In this paper, we explore the ability of evolutionary algorithms to design Boolean networks, abstract models of GRNs suitable for refining into synthetic biology implementations, and show how they can be used to control cell states within a range of executable models of biological systems.




</p><p><strong>Title: Evolving the Topology of Large Scale Deep Neural Networks
</strong><br><strong>Author:</strong>   Filipe Assunção,  Nuno Lourenço,  Penousal Machado,  Bernardete Ribeiro
<br><strong>Abstract:</strong> In the recent years Deep Learning has attracted a lot of attention due to its success in difficult tasks such as image recognition and computer vision. Most of the success in these tasks is merit of Convolutional Neural Networks (CNNs), which allow the automatic construction of features. However, designing such networks is not an easy task, which requires expertise and insight. In this paper we introduce DENSER, a novel representation for the evolution of deep neural networks. In concrete we adapt ideas from Genetic Algorithms (GAs) and Grammatical Evolution (GE) to enable the evolution of sequences of layers and their parameters. We test our approach in the well-known image classification CIFAR-10 dataset. The results show that our method: (i) outperforms previous evolutionary approaches to the generations of CNNs; (ii) is able to create CNNs that have state-of-the-art performance while using less prior knowledge (iii) evolves CNNs with novel topologies, unlikely to be designed by hand. For instance, the best performing CNNs obtained during evolution has an unexpected structure using six consecutive dense layers. On the CIFAR-10 the best model reports an average error of 5.87% on test data.






</p><p><strong>Title: Using GP is NEAT: Evolving Compositional Pattern Production Functions
</strong><br><strong>Author:</strong>   Filipe Assunção,  Nuno Lourenço,  Penousal Machado,  Bernardete Ribeiro
<br><strong>Abstract:</strong> The success of Artificial Neural Networks (ANNs) highly depends on their architecture and on how they are trained. However, making decisions regarding such domain specific issues is not an easy task, and is usually performed by hand, through an exhaustive trial-and-error process. Over the years, researches have developed and proposed methods to automatically train ANNs. One example is the HyperNEAT algorithm, which relies on NeuroEvolution of Augmenting Topologies (NEAT) to create Compositional Pattern Production Networks (CPPNs). CPPNs are networks that encode the mapping between neuron positions and the synaptic weight of the ANNs connection between those neurons. Although this approach has obtained some success, it requires meticulous parameterisation to work properly. In this article we present a comparison of different Evolutionary Computation methods to evolve Compositional Pattern Production Functions: structures that have the same goal as CPPNs, but that are encoded as functions instead of networks. In addition to NEAT three methods are used to evolve such functions: Genetic Programming (GP), Grammatical Evolution, and Dynamic Structured Grammatical Evolution. The results show that GP is able to obtain competitive performance, often surpassing the other methods, without requiring the fine tuning of the parameters.






</p><p><strong>Title: Structurally Layered Representation Learning: Towards Deep Learning through Genetic Programming
</strong><br><strong>Author:</strong>   Lino Rodriguez-Coayahuitl,  Alicia Morales-Reyes,  Hugo Jair Escalante
<br><strong>Abstract:</strong> We introduce a novel method for representation learning based on genetic programming (GP). Inspired into the way that deep neural networks learn descriptive/discriminative representations from raw data, we propose a structurally layered representation that allows GP to learn a feature space from large scale and high dimensional data sets. Previous efforts from the GP community for feature learning have focused on small data sets with a few input variables, also, most approaches rely on domain expert knowledge to produce useful representations. In this paper, we introduce the structurally layered GP formulation, together with an efficient scheme to explore the search space and show that this framework can be used to learn representations from large data sets of high dimensional raw data. As case of study we describe the implementation and experimental evaluation of an autoencoder developed under the proposed framework. 
Results evidence the benefits of the proposed framework and pave the way for the development of deep genetic programming.





