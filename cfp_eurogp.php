</p><p><strong>Title: Genetic Programming Hyper-heuristic with Cooperative Coevolution for Dynamic Flexible Job Shop Scheduling
</strong><br><strong>Author:</strong>   Daniel Yska,  Yi Mei,  Mengjie Zhang
<br><strong>Abstract:</strong> Flexible Job Shop Scheduling (FJSS) problem has many real-world applications such as manufacturing and cloud computing, and thus is an important area of study. In real world, the environment is often dynamic, and unpredicted job orders can arrive in real time. Dynamic FJSS consists of challenges of both dynamic optimisation and the FJSS problem. In Dynamic FJSS, two kinds of decisions (so-called routing and sequencing decisions) are to be made in real time. Dispatching rules have been demonstrated to be effective for dynamic scheduling due to their low computational complexity and ability to make real-time decisions. However, it is time consuming and strenuous to design effective dispatching rules manually due to the complex interactions between job shop attributes. Genetic Programming Hyper-heuristic (GPHH) has shown success in automatically designing dispatching rules which are much better than the manually designed ones. Previous works only focused on standard job shop scheduling with only the sequencing decisions. For FJSS, the routing rule is set arbitrarily by intuition. In this paper, we explore the possibility of evolving both routing and sequencing rules together and propose a new GPHH algorithm with Cooperative Co-evolution. Our results show that co-evolving the two rules together can lead to much more promising results than evolving the sequencing rule only.





</p><p><strong>Title: Geometric Crossover in Syntactic Space
</strong><br><strong>Author:</strong>   João Macedo,  Carlos Fonseca,  Ernesto Costa
<br><strong>Abstract:</strong> This paper presents a geometric crossover operator for Tree-Based Genetic Programming that acts on the syntactic space, where each expression tree is represented in prefix notation. The proposed operator is compared to the standard subtree crossover on a symbolic regression problem, on the Santa Fe Ant Trail and on a classification problem. Statistically validated results show that the individuals produced using this method are significantly smaller than those produced by the subtree crossover, and have similar or better performance in the target tasks.





</p><p><strong>Title: A Comparative Study on Crossover in Cartesian Genetic Programming
</strong><br><strong>Author:</strong>   Jakub Husa,  Roman Kalkreuth
<br><strong>Abstract:</strong> Cartesian Genetic Programming is often used with mutation as the sole genetic operator. Compared to the comprehensive and detailed knowledge about the effect and use of mutation in CGP, the use of crossover has been less investigated and studied. In this paper, we present a comparative study of previously proposed crossover techniques for Cartesian Genetic Programming. This work also includes the proposal of a new crossover technique which swaps block of the CGP phenotype between two selected parents. The experiments of our study open a new perspective on comparative studies on crossover in CGP and its challenges. Our results show that it is possible for a crossover operator to outperform the standard (1+lambda) strategy on a limited number of tasks. The question of finding a universal crossover operator in CGP remains open.




</p><p><strong>Title: Comparing Rule Evaluation Metrics for the Evolutionary Discovery of Multi-Relational Association Rules in the Semantic Web
</strong><br><strong>Author:</strong>   Minh Duc Tran,  Claudia d&#39;Amato,  Binh Thanh Nguyen,  Andrea G. B. Tettamanzi
<br><strong>Abstract:</strong> We carry out a comparison of popular asymmetric metrics, originally proposed for scoring association rules, as building blocks for a fitness function for evolutionary inductive programming. In particular, we use them to score candidate multi-relational association rules in an evolutionary approach to the enrichment of populated knowledge bases in the context of the Semantic Web. The evolutionary algorithm searches for hidden knowledge patterns, in the form of SWRL rules, in assertional data, while exploiting the deductive capabilities of ontologies.
Our methodology is to compare the number of generated rules and total predictions when the metrics are used to compute the fitness function of the evolutionary algorithm. This comparison, which has been carried out on three publicly available ontologies, is a crucial step towards the selection of suitable metrics to score multi-relational association rules that are generated from ontologies.






</p><p><strong>Title: Investigating a Machine Breakdown Genetic Programming Approach for Dynamic Job Shop Scheduling
</strong><br><strong>Author:</strong>   John Park,  Yi Mei,  Su Nguyen,  Gang Chen,  Mengjie Zhang
<br><strong>Abstract:</strong> Dynamic job shop scheduling (JSS) problems with dynamic job arrivals have been studied extensively in the literature due to their applicability to real-world manufacturing systems, such as semiconductor manufacturing. 
