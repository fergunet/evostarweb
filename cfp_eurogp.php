    <h2>Submission Details</h2>
    <p> Submissions must be original and not published elsewhere. They will be 
      peer reviewed by at least three members of the program committee. The reviewing 
      process will be double-blind, so please omit information about the authors 
      in the submitted paper. Submit your manuscript in <a href="http://www.springer.com/computer/lncs?SGWID=0-164-6-793341-0">Springer 
      LNCS format</a>. Page limit is 16 pages.</p>



    
<p>
      Authors of accepted papers will have to improve their paper on the basis 
      of the reviewers' comments and will be asked to send a camera ready version 
      of their manuscripts. At least one author of each accepted work has to register 
      for the conference, attend the conference and present the work.</p>
 </p>   

   <p>Submission link: <a href="https://myreview.saclay.inria.fr/eurogp19">https://myreview.saclay.inria.fr/eurogp19</a></p>

 <h2>Presentation details</h2>


<ul>
<li>Long talks: Your work will be presented in a 20 minute talk followed by
a 5 minute discussion round. We also encourage you to bring and present
a poster in the poster session to take advantage of a more informal and
direct feedback.</li>
<li>
Short talks (posters): Your work will be presented in a 10 minute talk
(no discussion round) and in a poster session. Please be aware that oral
and poster presentations are <strong>MANDATORY</strong>.
</li>
</ul>
