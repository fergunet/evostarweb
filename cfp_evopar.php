 <h2>Parallel Architectures and Distributed Infrastructures</h2>
    <p>There is growing interest in running evolutionary computation on Parallel 
      and Distributed Computing Infrastructures. A number of technologies are 
      already available. These include Grid and Cloud Computing, Internet Computing 
      (e.g. seti@home, boinc), General Purpose Computation on Graphics Processing 
      Units (GPGPU), multi-core and many-core architectures and supercomputers. 
      Although they are routinely used for running computing intensive applications, 
      considerable skill is required to get the best from them. The experimenter 
      has to consider scheduling, porting of applications, communication topologies, 
      new parallel models and architectures, cache and memory management optimization, 
      preemptive multitasking and simultaneous multi threading and even energy 
      consumption. Also, the experimenter may need to change their evolutionary 
      algorithm to fully exploit these new tools. At Evo<span class="red">Par</span> scientists and engineers 
      will gather to share and exchange their experiences, discuss challenges, 
      and report state-of-the-art and in-progress research on all aspects of the 
      application of evolutionary algorithms for improving parallel architectures 
      and distributed computing Infrastructures. Evo<span class="red">PAR</span> will assist the two-way 
      flow of ideas between the parallel computing community and the EC community. 
    </p>
    <h2>Areas of Interest and Contributions</h2>
    <p>High quality paper submissions which demonstrate novelty in terms of methodology, 
      application or both, were strongly encouraged. Applications of interest 
      included (but were not limited to)<br />
    </p>
    <ul>
      <li>Parallel implementation of evolutionary algorithms. </li>
      <li>Optimisation of Parallel architectures by means of EAs. </li>
      <li>Hardware implementation of EAs, including but not limited to Field Programmable 
        Gate Arrays. GP-GPU optimization. </li>
      <li>Improving Scheduling techniques for P2P and Grid Systems. Improving 
        Scheduling techniques for running distributed EAs. </li>
      <li>Improving Fault tolerance techniques for distributed systems and Distributed 
        EAs capabilities for coping with failures.</li>
      <li>Analytical modelling and performance evaluation of Parallel and Distributed 
        Infrastructures when running EAs.</li>
      <li>Case studies showing the role of Parallel and Distributed Infrastructures 
        in conjunction with Distributed EAs when solving hard real-life problems.</li>
    </ul>
