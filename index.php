<h3>FAQ</h3>

<h4>What are the conference dates?</h4>
EvoStar 2018 will be held from 9am on Wednesday 4 April until 2pm on Friday 6 April, with an optional Friday afternoon local excursion.


<h4>Where is the location Venue?</h4>

See the <a href="location_venue.php">Venue information page</a>.



<h4>Do I need to present a poster?</h4>
Authors who have been allocated a short talk must present a poster in the poster session. Authors with a long talk can optionally bring one too. 


<h4>How long are the talks?</h4>
A mixture of long (20 min + 5 min questions) and short (10 min, no questions) talks are presented over two and a half days, and your conference chair will inform you when your talk takes place and its duration.  If you are unclear, contact your programme chair to clarify these details. 
Contact details are at <a href="http://www.evostar.org/2018/about_organisers.php">http://www.evostar.org/2018/about_organisers.php</a>

